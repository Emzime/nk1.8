<center><big><b>Administration des News</b></big></center><br>
<li><b>Index</b> : Affiche la liste des news avec options de classement, Il permet d'éditer, de supprimer ou d'ajouter 
une news et de se rendre dans la gestion des catégories et les préférences.<br>
<br>
<li><b>Ajouter une news</b> : Pour ajouter une news, complétez simplement le formulaire. La partie suite est optionnel, 
utilisez la si vous désirez limiter l'affichage de la news en page d'accueil. Vous pouvez également choisir la date et 
l'heure de sa publication et voir un aperçu de votre news en cliquant sur le bouton "Prévisualisation".<br>
<br>
<li><b>Editer une news</b> : Vous permet d'éditer une news et d'en changer le titre, le contenu, la date ou encore la catégorie.<br>
<br>
<li><b>Gestion des catégories</b> : Cette partie permet de créer des catégories de news (sujet) avec
image et description de celle-ci. L'image sera affichée en 50X50 dans les news, il est donc conseillé
d'utiliser des images de cette dimension.<br>
<br>
<li><b>Préférences</b> : Vous permet de régler le nombre de news en page d'accueil et dans le module Archives.<br>
<br>
<br>
<i><b>Note :</b> La fonction d'upload n'est pas activée sur tous les hébergeurs et requière que le répertoire 
"upload/News" soit en CHMOD 777.<i>
