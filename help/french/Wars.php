<center><big><b>Gestion des Matches</b></big></center><br>
<li><b>Index</b> : Affiche la liste des matches en fonction de leur date et leur statut, il permet d'éditer, de supprimer 
ou d'ajouter un match et de se rendre dans les préférences.<br>
<br>
<li><b>Ajouter un match</b> : Ce formulaire est utilisé aussi bien pour rentrer un match "A jouer" qu'un match "Terminé". 
En 1er sélectionnez le statut du match et la Team, ensuite en fonction de celui-ci, rentrez les informations utiles. 
Dans le cas d'un match "A jouer" il vous faudra l'éditer une fois que vous l'aurez fait pour changer son statut en "Terminé".<br>
<br>
<li><b>Editer un match</b> : vous permet d'éditer toutes les informations d'un match, d'en changer le statut et d'ajouter/éditer
des fichiers joints (screenshots ou démo).<br>
<br>
<li><b>Préférences</b> : Vous permet de régler le nombre de matches par page dans le module Wars.<br>
<br>
<br>
<i><b>Note :</b> La fonction d'upload n'est pas activée sur tous les hébergeurs et requière que le répertoire 
