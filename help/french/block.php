<center><big><b>Gestion des Blocks</b></big></center><br>

<li><b>Index</b> : Dans cette partie, vous avez la liste des blocks sous forme d'un
tableau. Vous pouvez éditer ou supprimer un block mais aussi grâce aux flèches (position) 
monter ou descendre ce block. L'option Ajouter un block vous permet de créer un nouveau block. <br>
<br>
<li><b>Ajouter un block</b> : Entrez le titre, le niveau, le type de block et 
sélectionnez les modules ou vous voulez qu'il soit visible (par défaut il est visible partout). Une fois créé, vous serez redirigé vers l'édition 
de ce block..<br>
<br>
<li><b>Editer</b> : Lors de l'édition d'un block, vous pouvez modifier le titre,
régler le niveau (c'est à dire les membres autorisés à le voir), régler des options pour certains blocks, mettre ce block à
droite, à gauche, au centre ou encore en bas voir le désactiver et enfin sélectionner les modules ou le block sera visible 
(maintenir ctrl et cliquer sur les modules de la liste). Dans le cas d'un block HTML vous pouvez 
également modifier son contenu et dans le cas d'un block module, changer de module.<br>
<br>
<br>
<br>
<br>
<i><b>Note :</b> Pour modifier les liens des blocks menu, allez directement dans "Gestion des menus" ou cliquez sur "Edition du menu" dans l'édition du block.<i><br>
