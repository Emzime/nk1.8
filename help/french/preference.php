<center><big><b>Préférences générales</b></big></center><br>

<li><b>Titre du site</b> : Le nom de votre site qui sera inscrit en autre dans la barre de votre navigateur et les emails.<br>
<br>
<li><b>Slogan</b> : idem au titre du site.<br>
<br>
<li><b>Tag</b> : Les Tags de votre clan (ex : [Nk]) qui seront affiché en autre dans le module Team et match.<br>
<br>
<li><b>Url du Site</b> : La véritable Url de votre site (pas de redirection de type ULIMIT ou TK)<br>
<br>
<li><b>Email de l'admin</b> : L'email de l'administrateur qui recevra notamment les notifications d'inscription (si activées)<br> 
<br>
<li><b>Message de Bas de page</b> : Le message de copyright de votre site, vous pouvez y 
insérer une image (bannière) au besoin grâce au code html.<br> 
<br>
<li><b>Statut du site</b> : Vous permet d'ouvrir/fermer votre site aux membres et aux visiteurs pour y effectuer des mises à jour par exemple.<br> 
<br>
<li><b>Index du Site</b> : Vous pouvez ici choisir le module affiché par défaut à l'index du site.<br> 
<br>
<li><b>Thème</b> : Sélectionnez le thème par défaut que vous voulez utiliser pour votre site.<br> 
<br>
<li><b>Langue</b> : Sélectionnez la langue par défaut de votre site.<br> 
<br>
<li><b>Inscription</b> : Sélectionnez le statut des inscriptions : Ouverte, Fermée ou par mail (password crée par nuked-klan et envoyé par mail)<br> 
<br>
<li><b>Validation</b> : Sélectionnez le mode de validation des nouveaux membres : Automatique, par l'administrateur ou par email.<br> 
<br>
<li><b>Autoriser les membres...</b> : Vous permet d'autoriser ou pas les membres a supprimer leur propre compte.<br> 
<br>
<li><b>Nombre de membres par page</b> : Vous permet de régler le nombre de membres par page dans le module Members.<br> 
<br>
<li><b>Avatars</b> : Vous permet d'autoriser ou pas les avatars externes (lien web) et par upload.<br> 
<br>
<li><b>Inscription</b> : Cochez "Etre averti par mail" pour recevoir un email à chaque nouvelle inscription. Remplissez 
la charte - règlement pour l'afficher à l'inscription (optionnel) et le texte de l'email d'inscription pour 
envoyer un mail de bienvenue aux nouveaux membres (laissez vide pour désactiver ses fonctions).<br> 
<br>
<li><b>Statistiques </b> : Vous permet de régler le temps d'une visite et le niveau d'accès de l'analyse des visites.<br> 
<br>
<li><b>Nom du cookie</b> : A changer si plusieurs nuked-klan sont installés sur un même compte FTP.<br> 
<br>
<li><b>Durée en Jour...</b> : La durée de validité en jour d'une session par cookie (identification).<br> 
<br>
<li><b>Durée en minutes...</b> : La durée de validité en minutes d'une session provisoire par IP. Mettre à "0" 
pour désactiver cette option (pour les partages de connexion ou les réseaux local par exemple)<br> 
<br>
<li><b>Durée en secondes...</b> : La durée maximale pendant laquelle un visiteur ou un membre est considéré comme connecté. (Qui est en ligne)<br> 
<br>
<li><b>Mots clés</b> : Les mot clés de votre site (metatags) pour les moteurs de recherche.<br> 
<br>
<li><b>Description...</b> : La description de votre site (metatags) pour les moteurs de recherche.<br> 
